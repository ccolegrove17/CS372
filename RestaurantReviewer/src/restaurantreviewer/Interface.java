    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        Restaurant place = new Restaurant(nameField.getText(), addressField.getText(), notesArea.getText(), ratingSlider.getValue());//makes a new restaurant
        restaurants.add(place);//adds the restaurant to the arraylist
        try {//in case things go crazy
            BufferedWriter wrtr = new BufferedWriter(new FileWriter(filename, true));//makes a new writer
            String line = place.formatString();//calls the function of what to write
            wrtr.write(line);//writes the line to the file
            wrtr.newLine();//puts a new line
            wrtr.close();//closes the file
            textArea.append(line + "\n");//adds it to the text area
        } catch (Exception ex) {//hopefully we won't get here
            ;
        }
    }//GEN-LAST:event_submitButtonActionPerformed


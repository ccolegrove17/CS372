    /**
     * For when the user opens the window
     *
     * @param evt
     */
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {//just in case
            BufferedReader rdr = new BufferedReader(new FileReader(filename));//reads the file
            String line;
            String[] patterns = {"Name:(.*) Address:", "Address:(.*) Notes:", "Notes:(.*) Rating:", "Rating:(.*)"};//what the regex will search for
            String[] info = {"", "", "", ""};//info about the restaurant
            while ((line = rdr.readLine()) != null) {//while the file has more lines
                for (int i = 0; i < 4; i++) {//goes 4 times for the 4 info types
                    Pattern p = Pattern.compile(patterns[i]);//gets the pattern
                    Matcher m = p.matcher(line);//matches the pattern to the string
                    while (m.find()) {//finds the pattern
                        info[i] = m.group(1);//puts it into the array
                    }
                }
                Restaurant place = new Restaurant(info[0], info[1], info[2], Integer.parseInt(info[3]));//makes a new restaurant
                restaurants.add(place);//adds the restaurant to the arraylist
            }
            rdr.close();//closes the file
            textArea.setText(null);//clears the text area
            for (int i = 0; i < restaurants.size(); i++) {//for all the things in the arraylist
                String list = restaurants.get(i).formatString();//formats what it will output
                textArea.append(list + "\n");//add it to the textArea
            }

        } catch (Exception ex) {
            try {//in case things break
                BufferedWriter wrtr = new BufferedWriter(new FileWriter(filename, true));//makes an empty file
                wrtr.close();//closes the empty file
            } catch (Exception e) {//hopefully we won't get here
                System.out.println(e.getMessage());//outputs the error message
            }
        }
    }//GEN-LAST:event_formWindowOpened

